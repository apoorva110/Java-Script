//WAP FOR SWITCH CASE
let age = parseInt(prompt("Enter your age: "))
switch(age){
    case 11:
        alert("Your age is 11")
        break
    case 12:
        alert("Your age is 12")
        break
    case 13:
        alert("Your age is 13")
        break
    case 14:
        alert("Your age is 14")
        break
    case 15:
        alert("Your age is 15")
        break
    default:
        alert("You are alien")
}